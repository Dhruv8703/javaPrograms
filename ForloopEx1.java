
public class ForloopEx1 {

	public static void main(String[] args) {
		for(int a=1;a<5;a++) {
			System.out.println("Dhruv");
			
		}
		System.out.println("-----------");

	
	for(int a=1;a<=5;++a) {
   System.out.println("Dhruv");

	}
	System.out.println("----------");
	
	for(int a=1;a<=5;a+=1) {
		System.out.println("Dhruv");
	}
	System.out.println("---------");
	
	for(int a=5;a>=1;a--) {
		System.out.println("Dhruv");
	}
	System.out.println("---------");
	 
	for(int a=10;a<=15;a++) {
		System.out.println("Dhruv");
	}
	
	
	}
	}
