
public class PrintingEx {

	public static void main(String[] args) {
		int a= 10,b=5;
		System.out.println(a+"h");
		System.out.println(a+'h');
		System.out.println(a+"hi");
		System.out.println(a+"hi"+b);
		System.out.println(4+a+"hi"+b+2);
		System.out.println(a+'a'+"hi"+'a');

	}

}
